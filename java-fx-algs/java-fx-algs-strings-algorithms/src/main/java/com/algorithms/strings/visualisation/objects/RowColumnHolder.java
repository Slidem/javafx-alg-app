package com.algorithms.strings.visualisation.objects;

/**
 * @author Mihai Alexandru
 * @date 20.10.2018
 */
public class RowColumnHolder {

    private int row;

    private int column;

    public RowColumnHolder(int row, int column) {
        this.row = row;
        this.column = column;
    }

    public int getRow() {
        return row;
    }

    public int getColumn() {
        return column;
    }
}
